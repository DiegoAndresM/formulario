    <p class="pa">Nacido en Málaga, pintó su primer cuadro a la edad de 8 años: "El picador amarillo". Pronto mostró una habilidad impresionante para su edad, era capaz de pintar gran cantidad de figuras ciñéndose con naturalidad a las normas más conservadoras de la composición académica. <br><br>

     El estilo de Picasso no puede ser encuadrado en una lista finita, ya que no solo fue un gran aporte para los principales movimientos modernistas europeos, sino que ciertas vertientes se le atribuyen sólo a él (ej: protocubismo, periodo azul y el minotauro). <br><br>

    El éxito de Picasso es un crisol de muchos factores: su genialidad, las escuelas donde estudió (Barcelona y París principalmente), su interés por lo social, su amistad con artistas y marchantes, su producción prolífica de cuadros, la mentalidad innovadora y su liderazgo. Si bien al final de su carrera tendía a pintar figuras cada vez más simples, rozando lo abstracto, su autoridad como artista era incuestionable, especialmente por su bagaje en obras de gran factura. Picasso se ganó el derecho a romper las reglas, ya que logró dominarlas a la perfección. <br><br>

